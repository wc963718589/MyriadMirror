package com.example.myriadmirror.model

data object ChatConfig {
    var model: String = ""
    var temperature: Double = 0.0   // 使用什么采样温度，介于 0 和 2 之间。较高的值（如 0.8）将使输出更加随机，而较低的值（如 0.2）将使输出更加集中和确定
    var topP: Double = 0.0          // 一种替代温度采样的方法，称为核采样，其中模型考虑具有 top_p 概率质量的标记的结果。所以 0.1 意味着只考虑构成前 10% 概率质量的标记
}
